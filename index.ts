// A type is an easy way to refer to the different properties and functions that a value has
// A value is anything we can assign to a variable (function, array, class etc)
// Every value in typescript has a type  

// Two kinds of types:
  // Primitive types:
    // Number 
    // Boolean
    // Void
    // Undefined
    // String 
    // Symbol
    // Null
  
  // Object types:
    // Functions 
    // Arrays
    // Classes
    // Objects

// Types are used by the TypeScript compiler to analyse our code for errors and allows other developers to understand what values are defined in our codebase
